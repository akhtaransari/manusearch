package com.makersharks.ManuSearch.enums;

/**
 * Enum representing various manufacturing processes.
 */
public enum ManufacturingProcess {
    /**
     * Process of shaping material using molds.
     */
    MOULDING,

    /**
     * Process of creating objects using 3D printing technology.
     */
    _3D_PRINTING,

    /**
     * Process of forming objects by pouring liquid material into molds.
     */
    CASTING,

    /**
     * Process of applying a protective or decorative layer to an object.
     */
    COATING
}
